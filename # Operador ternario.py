# Operador ternario
print ('Programa que devuelve si eres mayor o menor de edad')
# Pedimos al usuario que ingrese su edad y lo convertimos en entero
edad=int(input('Escribe tu edad: \n'))
# Realizamos la condicion en una sola linea
print ('Eres mayor de edad' if edad>18 else 'Eres menor de edad')